package unary_operator;

public class UnOperator {
    public static void main(String[] args) {
        int a = 20;
        boolean b = true;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(!b);

    }
}
