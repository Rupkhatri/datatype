package Operators;

public class JavaOperators {
    public static void main(String[] args) {
        int a = 15;
        int b = 25;
        int c;
        System.out.println(c = a);
        System.out.println(b += a);
        System.out.println(b -= a);
        System.out.println(b *= a);
        System.out.println(b /= a);
        System.out.println(b %= a);
        System.out.println(b ^= a);

    }
}
